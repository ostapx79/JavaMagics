package org.example.variables_lesson2;

public class VariablesAndDataTypes {
    public static void main(String[] args) {

        // переменная типа String
        String nameKonstantin;
        nameKonstantin = "Konstantin";
        String ronaldWeasley = "Ronald Weasley";
        // **[тип] [название]** *= [значение]*
        float result;                    // объявление переменной
        String name = "Ronald Weasley";  // объявление и инициализация переменной
        int b, c = 4, d;                 // объявление нескольких переменных
        // byte d = 1;

        int def_123 = 5;                 // правильное название переменной
        // неправильное название переменной
        // int 123abc = 123;
        float piF = 3.14f;
        double eD = 2.718281828;
        int aI = 3228;
        // long bl = 12345678900987654321L;
        long bl = 2345678900987654321L;

    }
}
